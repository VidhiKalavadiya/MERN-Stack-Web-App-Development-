function checkSwitchCase(value) {
  switch (value) {
      case 1:
          console.log("Case 1 executed");
          break;
      case 2:
          console.log("Case 2 executed");
          break;
      case 3:
          console.log("Case 3 executed");
          break;
      default:
          console.log("Default case executed");
  }
}

// Example usage:
checkSwitchCase(2);
